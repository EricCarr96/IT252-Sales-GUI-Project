    private void evalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_evalButtonActionPerformed
       SalesReader sr = new SalesReader();
        try {
            List<SalesRep> sales = sr.readSales("Stars.txt");
            for (SalesRep s : sales)
            if(s.getBooksSold() + s.getPaperSold() + s.getSuppliesSold() >= 8000)
            {
              StarsFile sFile = new StarsFile();
              sFile.writeStar(s);
               
               firstNameInput.setText("");
                lastNameInput.setText("");
                salesIdInput.setText("");
                booksSoldInput.setText("");
                suppliesSoldInput.setText("");
                paperSoldInput.setText("");
                nButton.setSelected(false);
                sButton.setSelected(false);
                eButton.setSelected(false);
                wButton.setSelected(false);
                emButton.setSelected(false);
                pButton.setSelected(false);
                vButton.setSelected(false);
                
                
              salesOutput.append(s.toString());
             salesOutput.append("\n");
            }
                
            
                
             
            
        } catch (IOException ex) 
        {
            JOptionPane.showMessageDialog(this, "Unable to open file");
            Logger.getLogger(SalesApp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_evalButtonActionPerformed


    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
       
        boolean allDataRecieved = true;
        SalesRep s = new SalesRep();
        
        String name = firstNameInput.getText();
       if(name.trim().length() == 0)
       {
           JOptionPane.showMessageDialog(this, " You must enter a valid First name ");
           allDataRecieved = false;
       }else
       {
       firstNameInput.setText(name.trim());
       s.setFirstName(name.trim());
       
       }
       
       String lastName = lastNameInput.getText();
       if(lastName.trim().length() == 0)
       {
           JOptionPane.showMessageDialog(this, " You must provide Last name ");
           allDataRecieved = false;
           
       }else
       {
       lastNameInput.setText(lastName.trim());
       s.setLastName(lastName.trim());
       
       }
       
       String ID = salesIdInput.getText();
       try
       {
           int repID = Integer.parseInt(ID);
           if(repID < 1)
           {
           
            JOptionPane.showMessageDialog(this, " ID cannot be less than 1 ");
           allDataRecieved = false;
           
           }else
           {
               s.setRepID(repID);
           }
       }catch (NumberFormatException e)
       {
           JOptionPane.showMessageDialog(this, " ID must be an integer ");
           allDataRecieved = false;
       } 
       
       String sbook = booksSoldInput.getText(); 
       try{
         
           double book = Double.parseDouble(sbook);
           if (book < 1)
           {
                 JOptionPane.showMessageDialog(this, " Books Sold cannot be less than 1 ");
           allDataRecieved = false;
           
           }else
           {
           s.setBooksSold(book);
           }
       }catch (NumberFormatException e)
       {
       
           JOptionPane.showMessageDialog(this, " Books Sold must be an integer ");
           allDataRecieved = false;
       }
       
       String sSold = suppliesSoldInput.getText();
       try{
           double supplies =  Double.parseDouble(sSold);
           if(supplies < 1)
           {
               JOptionPane.showMessageDialog(this, " Office Supplies Sold cannot be less than 1 ");
           allDataRecieved = false;
           
           }else
           {
           s.setSuppliesSold(supplies);
           
           }
       
       }catch(NumberFormatException e)
       {
       
           JOptionPane.showMessageDialog(this, " Office Supplies Sold must be an integer ");
           allDataRecieved = false;
       }
       String pSold = paperSoldInput.getText();
       try{
           double paper = Double.parseDouble(pSold);
           if(paper < 1)
           {
               JOptionPane.showMessageDialog(this, " Paper Sold cannot be less than 1 ");
           allDataRecieved = false;
           
           }else
           {
           s.setPaperSold(paper);
           }
       
       }catch(NumberFormatException e)
       {
       
             JOptionPane.showMessageDialog(this, " Paper Sold must be an integer ");
           allDataRecieved = false;
       }
       
       
       if(emButton.isSelected())
       {
       s.setContact("Email ");
       }else if(pButton.isSelected())
       {
           s.setContact("Phone ");
            
    
       
       }else if(vButton.isSelected())
       {
           
       s.setContact("visit ");
       }else
       {
       JOptionPane.showMessageDialog(this, " Please select a contact method ");
       allDataRecieved = false;
       }
               
        if (nButton.isSelected())
        {
        s.setDistrict("North ");
        
        }else if(sButton.isSelected())
        {
        s.setDistrict("South ");
        }else if(eButton.isSelected())
        {
        s.setDistrict("East ");
        }else if(wButton.isSelected())
        {
        s.setDistrict("West ");
        }else
        {
        JOptionPane.showMessageDialog(this, " Please select a valid District ");
        allDataRecieved = false;
        }
               
       if(allDataRecieved)
       
       {
      
        
            try {
                SalesFileStorage sfs = new SalesFileStorage();
            
                sfs.writeSales(s);
                statusLabel.setText("Succesfully Submitted!");
                firstNameInput.setText("");
                lastNameInput.setText("");
                salesIdInput.setText("");
                booksSoldInput.setText("");
                suppliesSoldInput.setText("");
                paperSoldInput.setText("");
                nButton.setSelected(false);
                sButton.setSelected(false);
                eButton.setSelected(false);
                wButton.setSelected(false);
                emButton.setSelected(false);
                pButton.setSelected(false);
                vButton.setSelected(false);
                
            } catch (IOException ex) {
                
                  JOptionPane.showMessageDialog(this, " Error cannot find File ");
                Logger.getLogger(SalesApp.class.getName()).log(Level.SEVERE, null, ex);
            }
        
       }
       
    }//GEN-LAST:event_submitButtonActionPerformed

